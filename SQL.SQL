CREATE TABLE CARTAO (
  ID INT NOT NULL AUTO INCREMENT,
  TITULAR VARCHAR(250) NOT NULL,
  NUMERO VARCHAR (50) NOT NULL,
  CHAVESEGURANCA INT NOT NULL,
  USUARIOID INT NOT NULL,
  PRIMARY KEU (ID),
  CONSTRAINT FK_CARTAO_USUARIO FOREIGN KEY USUARIOID
  REFERENCES USUARIO(ID)
);
CREATE TABLE PRODUTORA(
	ID INT NOT NULL AUTO INCREMENT,
	NOME VARCHAR(250) NOT NULL;
	PRIMARY KEY (ID)
);
CREATE TABLE LIKE(
	ID INT NOT NULL AUTO INCREMENT,
	NIVEL INT NOT NULL;
	PRIMARY KEY (ID)
);
CREATE TABLE SERIE(
  ID INT NOT NULL,
  NOME VARCHAR(250) NOT NULL,
  PRODUTORAID INT NOT NULL,
  CATEGORIAID INT NOT NULL,
  LIKEID INT NOT NULL,
  CONSTRAINT FK_SERIE_PRODUTORA FOREIGN KEY PRODUTORAID
  REFERENCES PRODUTORA(ID),
  CONSTRAINT FK_SERIE_CATEGORIA FOREIGN KEY CATEGORIAID
  REFERENCES CATEGORIA(ID),
  CONSTRAINT FK_SERIE_LIKE FOREIGN KEY LIKEID
  REFERENCES LIKE(ID)
  
);
CREATE TABLE TEMPORADA (
  ID INT NOT NULL AUTO INCREMENT,
  NOME VARCHAR(250) NOT NULL,
  SEQUENCIAL INT NOT NULL,
  SERIEID INT NOT NULL,
  PRIMARY KEY(ID),
  CONSTRAINT FK_TEMPORADA_SERIE FOREIGN KEY SERIEID
  REFERENCES SERIE(ID)
);
CREATE TABLE FILME(
  ID INT NOT NULL,
  NOME VARCHAR(250) NOT NULL,
  DURACAO VARCHAR(250) NOT NULL,
  LANCAMENTODATE DATE NOT NULL,
  PRODUTORAID INT NOT NULL,
  CATEGORIAID INT NOT NULL,
  LIKEID INT NOT NULL,
  PRIMARY KEY
  CONSTRAINT FK_FILME_PRODUTORA FOREIGN KEY PRODUTORAID
  REFERENCES PRODUTORA(ID),
  CONSTRAINT FK_FILME_CATEGORIA FOREIGN KEY CATEGORIAID
  REFERENCES CATEGORIA(ID),
  CONSTRAINT FK_FILME_LIKE FOREIGN KEY LIKEID
  REFERENCES LIKE(ID)
  
);
CREATE TABLE COMENTARIOS(
  ID INT NOT NULL AUTO INCREMENT,
  USUARIOID INT NOT NULL,
  FILMEID INT NULL,
  SERIEID INT NULL,
  PRIMARY KEY(ID),
  DESCRICAO TEXT NOT NULL,
  CONSTRAINT FK_COMENTARIOS_USUARIO FOREIGN KEY USUARIOID
  REFERENCES USUARIO(ID),
  CONSTRAINT FK_COMENTARIOS_FILME FOREIGN KEY FILMEID
  REFERENCES FILME(ID),
  CONSTRAINT FK_COMENTARIOS_SERIE FOREIGN KEY SERIEID
  REFERENCES SERIE(ID)
);


